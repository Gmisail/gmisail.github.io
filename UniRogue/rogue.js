var player = {
    x: 2,
    y: 2,
    power: 1,
    down: function(){
        if(map[player.y + 1][player.x] == 0){
            player.y += 1;
            refresh();
        }
    },
    up: function(){
        if(map[player.y - 1][player.x] == 0){
            player.y -= 1;
            refresh();
        }
    },
    left: function(){
        if(map[player.y][player.x + 1] == 0){
            player.x += 1;
            refresh();
        }
    },
    right: function(){
        if(map[player.y][player.x - 1] == 0){
            player.x -= 1;
            refresh();
        }
    }
};

function render() {
    console_send_message_focus("Power: " + player.power);
    for (var y = 0; y < 25; y++) {

        console_print_newline();

        for (var x = 0; x < 25; x++) {
            map[player.y][player.x] = 2;

            if (map[y][x] == 1) {
                console_print_char_white("X");
            } else if (map[y][x] == 2) {
                console_print_char_blue("@");
            } else if (map[y][x] == 3) {
                console_print_char_green("*");
            } else if (map[y][x] == 4) {
                console_print_char_green("$");
            } else if (map[y][x] == 5) {
                console_print_char_green("/");
            } else if (map[y][x] == 6) {
                console_print_char_green("C");
            } else {
                console_print_space();
            }
        }
    }



}

function clear(){
    console_clear_screen();
}

function refresh(){
    clear();
    console();
    render();
    console_render();
}

refresh();

function console(){
    command_prompt.onsubmit = function(e){
        var message = command_prompt_input.value;
        e.preventDefault();

        switch(message){
            case "open":
                if(map[player.y - 1][player.x] == DOOR)
                        map[player.y - 1][player.x] = 0;
                if(map[player.y + 1][player.x] == DOOR)
                        map[player.y + 1][player.x] = 0;

                if(map[player.y][player.x + 1] == DOOR)
                        map[player.y][player.x + 1] = 0;

                if(map[player.y][player.x - 1] == DOOR)
                        map[player.y][player.x - 1] = 0;

                refresh();
        }

        command_prompt_input.value = "";
    }
}


document.onkeydown = function(e){
    map[player.y][player.x] = 0;

    if(e.keyCode == DOWN){
        pause = false;
        player.down();
    }

    if(e.keyCode == UP){
        pause = false;
        player.up();
    }

    if(e.keyCode == LEFT){
        pause = false;
        player.right();
    }

    if(e.keyCode == RIGHT){
        pause = false;
        player.left();
    }
}